import Foundation

/// 실제 사용되는 간격
/// Token뒤에 실제 사이즈가 숨기는것임으로 노출하지 않습니다.
enum SpacingSize: CGFloat {
    /// 2
    case Spacing_xxxs = 2
    /// 4
    case Spacing_xxs = 4
    /// 8
    case Spacing_xs = 8
    /// 12
    case Spacing_s = 12
    /// 16
    case Spacing_m = 16
    /// 20
    case Spacing_l = 20
    /// 24
    case Spacing_xl = 24
    /// 32
    case Spacing_xxl = 32
    /// 40
    case Spacing_xxxl = 40
}
